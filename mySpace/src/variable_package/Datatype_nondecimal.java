package variable_package;

public class Datatype_nondecimal {

	public static void main(String[] args)
	{
		// variable declaration- depend on variable use data-type
		
		// Use of byte datatype
		byte num;
		
		// variable initialization
		
		num=126;	// byte datatype used for range between -128 to 127
		
		// Usage 
		
		System.out.println("Value of Number is  "+num);
		
		// Use of short datatype 
		
		short value;	// this datatype is used between range of -32768 to 32767
		
		value=1005;
		
		System.out.println("Value is "+value);
		
		
		// Use of Integer datatype
		
		int digit;	// We use this datatype regularly for non-decimal value
		
		digit=39000;
		
		System.out.println(digit);
		
		//use of long datatype
		long  num1;
		
		num1=155000000;
		
		System.out.println(num1);
		
		// How to use string
		
		String Name;
		Name="Ashish";
		
		System.out.println("My name is "+Name+". What is your name");
		
		
	}

}
